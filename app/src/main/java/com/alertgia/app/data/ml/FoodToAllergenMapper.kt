    }

    fun mapToAllergenResult(
        predictions: List<Pair<String, Float>>,
        userAllergies: List<Allergy>,
        confidenceThreshold: Float = 0.15f
    ): AllergenResult {
        val confidentPredictions = predictions.filter { it.second >= confidenceThreshold }

        if (confidentPredictions.isEmpty()) {
            return AllergenResult(
                safetyLevel = SafetyLevel.WARNING,
                detectedAllergens = emptyList(),
                allIngredients = emptyList(),
                confidence = "LOW",
                explanation = "Offline: Could not confidently identify this food item.",
                recommendations = "Unable to determine allergens. Please check ingredients manually."
            )
        }

        val allDetectedFoods = confidentPredictions.map { it.first.replace("_", " ") }

        val potentialAllergens = confidentPredictions
            .flatMap { (label, _) -> getPotentialAllergens(label) }
            .toSet()

        val userAllergenNames = userAllergies.map { it.name.lowercase() }.toSet()
        val matchedAllergens = potentialAllergens
            .filter { it.lowercase() in userAllergenNames }

        val topConfidence = predictions.firstOrNull()?.second ?: 0f
        val confidenceStr = when {
            topConfidence > 0.7f -> "HIGH"
            topConfidence > 0.4f -> "MEDIUM"
            else -> "LOW"
        }

        val safetyLevel = when {
            matchedAllergens.isEmpty() -> SafetyLevel.SAFE
            matchedAllergens.any { allergen ->
                userAllergies.any {
                    it.name.equals(allergen, ignoreCase = true) && it.severity == Severity.SEVERE
                }
            } -> SafetyLevel.DANGER
            else -> SafetyLevel.WARNING
        }

        val topFood = allDetectedFoods.firstOrNull() ?: "unknown food"

        return AllergenResult(
            safetyLevel = safetyLevel,
            detectedAllergens = matchedAllergens.toList(),
            allIngredients = allDetectedFoods,
            confidence = confidenceStr,
            explanation = "Offline: Detected $topFood. " +
                "Allergens identified from known food composition database.",
            recommendations = when {
                confidenceStr == "LOW" ->
                    "Low confidence detection. Verify ingredients manually or switch to online mode."
                matchedAllergens.isNotEmpty() ->
                    "Potential allergens found. Verify with the actual ingredient list."
                else -> ""
            }
        )
    }
